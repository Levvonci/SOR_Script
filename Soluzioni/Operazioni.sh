# definizione di operazioni standard: Addizione, Sottrazione, Moltiplicazione, Divisione, Modulo
echo "Questa è l'addizione"

Addizione=$((5 + 5))
echo $Addizione

echo "----"

echo "Questa è la sottrazione"

Sottrazione=$((4 - 2))
echo $Sottrazione

echo "----"

echo "Questa è la moltiplicazione"

Moltiplicazione=$((5 * 5))
echo $Moltiplicazione

echo "----"

echo "Questa è la divisione"

Divisione=$((32 / 2))
echo $Divisione

echo "----"

echo "Questo è il modulo"

Modulo=$((34 % 3))
echo $Modulo
